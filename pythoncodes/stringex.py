disc="talib is a good boy."
print(disc[4]) #for print single character
print(disc[0:5]) #for print a group of char
print(len(disc)) #for print length of string
print(disc[0:5:2]) #for print a string in a skip sequence
print(disc[::-1]) #for reverse string
print(disc.isalnum()) #for checking a string is alpha numeric or not ex- aghdghdg123nsn
print(disc.endswith(".")) #for checking string is end with 
print(disc.count("o"))  #count char in string
print(disc.capitalize()) #for making string capitalize
print(disc.find("is")) #for find in string
print(disc.upper()) #for making string in upper/lower case
print(disc.replace("good","bad"))